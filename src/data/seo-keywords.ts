// Utility function to get keywords for a specific page
export function getKeywordsForPage(page: keyof typeof seoKeywords): string {
  const config = seoKeywords[page];
  if (!config) return "";
  
  return [
    ...config.primary,
    ...config.secondary,
    ...config.longTail.slice(0, 3), // Limit long tail for meta keywords
    ...config.local.slice(0, 2),    // Limit local keywords
  ].join(", ");
}

// Utility function to get all keyword variations for content optimization
export function getAllKeywordVariations(page: keyof typeof seoKeywords): string[] {
  const config = seoKeywords[page];
  if (!config) return [];
  
  return [
    ...config.primary,
    ...config.secondary,
    ...config.longTail,
    ...config.local,
    ...config.misspellings,
    ...config.englishVariations
  ];
}